









public class SquarePattern {

    public static void main(String[] args) {
        int rows=5;
        int column = 5;


        for(int i=0;i<column;i++){
            
            for (int j=0 ; j<rows; j++){

                System.out.print(i+1+"  ");
            }
            System.out.println();
        }
        


        
       
        
    }
    
}
